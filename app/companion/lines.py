def pick_companion(rel: str, kind: str, message: str) -> str:
    rel = rel if rel in VALID_RELATIONSHIPS else "bestie"
    lines = companion_lines()[rel].get(kind) or companion_lines()["bestie"]["chat"]
    if kind == "chat":
        msg = (message or "").strip()
        if any(k in msg for k in ("搞定", "完成", "做完", "好了", "写完")):
            pool = companion_lines()[rel]["task_done"]
            return random.choice(pool)
        if any(k in msg for k in ("烦", "不想", "累", "拖", "焦虑", "慌")):
            pool = companion_lines()[rel]["anxiety"]
            return random.choice(pool)
    return random.choice(lines)


